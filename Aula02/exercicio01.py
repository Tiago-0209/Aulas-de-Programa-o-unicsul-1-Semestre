ladoA = float(input("digite o tamanho do comprimento do retangulo: "))
ladoB = float(input("digite o tamanho da largura do retangulo: "))

area = ladoA*ladoB 
perimetro = (ladoA * 2) + (ladoB * 2)

print(f"a area do retangulo é igual a: {area}m²")
print(f"o perimetro do retangulo é igual a: {perimetro}m")